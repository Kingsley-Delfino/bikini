package com.springboot.bikini.model;

public class EmployeeDomain {
    private int employeeId;
    private String employeeName;
    private String employeePassword;
    private String employeeTel;
    private int employeeAge;
    private String employeeGender;
    private String employeeUniversity;
    private String employeeEmail;
    private String employeeAddress;
    private String employeeCvAddress;
    private String employeePhotoAddress;

    public int getEmployeeId() {
        return employeeId;
    }

    public void setEmployeeId(int employeeId) {
        this.employeeId = employeeId;
    }

    public String getEmployeeName() {
        return employeeName;
    }

    public void setEmployeeName(String employeeName) {
        this.employeeName = employeeName;
    }

    public String getEmployeePassword() {
        return employeePassword;
    }

    public void setEmployeePassword(String employeePassword) {
        this.employeePassword = employeePassword;
    }

    public String getEmployeeTel() {
        return employeeTel;
    }

    public void setEmployeeTel(String employeeTel) {
        this.employeeTel = employeeTel;
    }

    public int getEmployeeAge() {
        return employeeAge;
    }

    public void setEmployeeAge(int employeeAge) {
        this.employeeAge = employeeAge;
    }

    public String getEmployeeGender() {
        return employeeGender;
    }

    public void setEmployeeGender(String employeeGender) {
        this.employeeGender = employeeGender;
    }

    public String getEmployeeUniversity() {
        return employeeUniversity;
    }

    public void setEmployeeUniversity(String employeeUniversity) {
        this.employeeUniversity = employeeUniversity;
    }

    public String getEmployeeEmail() {
        return employeeEmail;
    }

    public void setEmployeeEmail(String employeeEmail) {
        this.employeeEmail = employeeEmail;
    }

    public String getEmployeeAddress() {
        return employeeAddress;
    }

    public void setEmployeeAddress(String employeeAddress) {
        this.employeeAddress = employeeAddress;
    }

    public String getEmployeeCvAddress() {
        return employeeCvAddress;
    }

    public void setEmployeeCvAddress(String employeeCvAddress) {
        this.employeeCvAddress = employeeCvAddress;
    }

    public String getEmployeePhotoAddress() {
        return employeePhotoAddress;
    }

    public void setEmployeePhotoAddress(String employeePhotoAddress) {
        this.employeePhotoAddress = employeePhotoAddress;
    }
}
